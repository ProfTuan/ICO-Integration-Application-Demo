    private void btnInstancesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInstancesActionPerformed
        // TODO add your handling code here:
        
        InputOutput io = IOProvider.getDefault().getIO("Status", false);
        io.reset();
        io.show();
        
        OutputWriter writer = io.getOut();

        Main m = new Main();
        m.initUseCaseModelFromPath(this.txtUseCase4Model.getText());
        
        writer.flush();
        writer.println("Ontology model for USE CASE 4 example added...\n");
        
        if(u4 == null){
            u4 = UseCaseFour.getInstance();
        }
        
        u4.addInformedConsentInstances();
        
        writer.println("*** INSTANCES ADDED ***", OutputColor.rgb(255, 0, 0));
        
        writer.close();
        
        lblInstances.setVisible(true);
        
        this.btnInstances.setEnabled(false);
    }//GEN-LAST:event_btnInstancesActionPerformed

