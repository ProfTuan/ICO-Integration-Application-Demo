    private void btnReasonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReasonActionPerformed
        // TODO add your handling code here:
        
        InputOutput io = IOProvider.getDefault().getIO("Status", false); 
        io.show();
        
        if(u4 == null){
            u4 = UseCaseFour.getInstance();
        }
        
        OutputWriter writer = io.getOut();
        u4.startReasoner();
        
        writer.println("\n** USE CASE 4 REASONING DEMO **\n");
        
        //What was the authorization for as a result of subject agreeing?
        writer.println("** What was the authorization for as a result of subject agreeing? **", OutputColor.rgb(0, 0, 255));
        writer.println("\t" + "Reasoned answer: ");
        List<MutableTriple<String, String, String>> answer1 = u4.whatAuthorizationIsGiven();
        for(MutableTriple <String, String, String> triple: answer1){
            writer.println("\t\t" + triple.getLeft() + " > " + triple.getMiddle() + " > " + triple.getRight());
        }
        
        //What are the details of the given authorization?
        writer.println("** What are the details of the given authorization? **", OutputColor.rgb(0, 0, 255));
        writer.println("\t" + "Reasoned answer: ");
        
        List<MutableTriple<String, String, String>> answer2 = u4.whatAreDetailsofAuthorization();
        
        for(MutableTriple<String, String, String> triple: answer2){
            writer.println("\t\t" + triple.getLeft() + " > " + triple.getMiddle() + " > " + triple.getRight());
        }
        
        writer.close();
        
        this.btnReason.setEnabled(false);
    }//GEN-LAST:event_btnReasonActionPerformed

