    private void btnInstancesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInstancesActionPerformed
        // TODO add your handling code here:
        

        InputOutput io = IOProvider.getDefault().getIO("Status", false);
        io.reset();
        io.show();
        
        OutputWriter writer = io.getOut();

        
        
        Main m = new Main();
        m.initUseCaseModelFromPath(this.txtUseCaseTwoModel.getText());
        //m.initUseCase2Model();
        
        writer.flush();
        
        writer.println("Ontology model for USE CASE 2 example added...\n");
        
        if(u2 == null){
            u2 = UseCaseTwo.getInstance();
        }
        
        u2.addInformedConsentInstances();
        
        writer.println("*** INSTANCES ADDED ***", OutputColor.rgb(255, 0, 0));
        //writer.println("Under construction");
        writer.close();
        
        lblInstances.setVisible(true); btnInstances.setEnabled(false);
    }//GEN-LAST:event_btnInstancesActionPerformed

