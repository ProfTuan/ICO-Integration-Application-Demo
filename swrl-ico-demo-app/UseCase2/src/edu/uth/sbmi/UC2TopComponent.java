    private void btnReasonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReasonActionPerformed
        // TODO add your handling code here:
        InputOutput io = IOProvider.getDefault().getIO("Status", false); 
        io.show();
        
        if(u2 == null){
         u2 = UseCaseTwo.getInstance();   
        }
        
         
        OutputWriter writer = io.getOut();
        
        u2.startReasoner();
        
        writer.println("\n** USE CASE 2 REASONING DEMO **\n");
        
        List<MutablePair<String, String>> whatIsSubjectResponsibilities = u2.whatIsSubjectResponsibilities();
        
        writer.println("** What is the responsibility of the participant subject? **", OutputColor.rgb(0, 0, 255));
        writer.println("\t" + "Reasoned answer: ");
        
        for(MutablePair<String,String> response1 : whatIsSubjectResponsibilities){
            writer.println("\t\t" + response1.getRight() + " ( " + response1.getLeft() + " ) ");
        }
        
        List<MutableTriple<String, String, String>> whoInformedSubjectAndTheirRoles = u2.whoInformedSubjectAndTheirRoles();
        
        writer.println("** Who Informed the subject and their role in the study? **", OutputColor.rgb(0, 0, 255));
        writer.println("\t" + "Reasoned answer: ");
        
        for(MutableTriple<String, String, String> triple : whoInformedSubjectAndTheirRoles){
            writer.println("\t\t" + triple.getMiddle()+ " ( " + triple.getRight() + ", " + triple.getLeft()+ " ) ");
        }
        
        writer.close(); btnReason.setEnabled(false);
    }//GEN-LAST:event_btnReasonActionPerformed

