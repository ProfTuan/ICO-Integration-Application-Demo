    private void btnInstancesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInstancesActionPerformed
        // TODO add your handling code here:
        
        InputOutput io = IOProvider.getDefault().getIO("Status", false);
        io.reset();
        io.show();
        
        OutputWriter writer = io.getOut();
        
        Main m = new Main();
        m.initUseCaseModelFromPath(this.txtUseCase3Model.getText());
        
        writer.flush();
        writer.println("Ontology model for USE CASE 3 example added...\n");
        
        if(u3 == null){
            u3 = UseCaseThree.getInstance();
        }
        
        u3.addInformedConsentInstances();
        
        writer.println("*** INSTANCES ADDED ***", OutputColor.rgb(255, 0, 0));
        
        writer.close();
        
        lblInstances.setVisible(true); this.btnInstances.setEnabled(false);
        
    }//GEN-LAST:event_btnInstancesActionPerformed

