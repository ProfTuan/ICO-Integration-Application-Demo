    private void btnReasonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReasonActionPerformed
        // TODO add your handling code here:
        
        InputOutput io = IOProvider.getDefault().getIO("Status", false); 
        io.show();
        
        if(u3 == null){
            u3 = UseCaseThree.getInstance();
        }
        
        OutputWriter writer = io.getOut();
        
        u3.startReasoner();
        
        writer.println("\n** USE CASE 3 REASONING DEMO **\n");
        
        //What authorization was given as a result of the subject's agreement?
        
        List<MutableTriple<String, String, String>> answer1 = u3.whatIsTheAuthorizationFor();
        
        writer.println("** What authorization was given as a result of the subject's agreement? **", OutputColor.rgb(0, 0, 255));
        writer.println("\t" + "Reasoned answer: ");
        
        for(MutableTriple <String, String, String> triple : answer1){
            writer.println("\t\t" + triple.getLeft() + " > " + triple.getMiddle() + " > " + triple.getRight());
        }
        
        List<MutableTriple<String, String, String>> answer2 = u3.whatAreTheyRestrictedFor();
        
        writer.println("** What are the details of the given authorization? **", OutputColor.rgb(0, 0, 255));
        writer.println("\t" + "Reasoned answer: ");
        
        for(MutableTriple <String, String, String> triple : answer2){
            writer.println("\t\t" + triple.getLeft() + " > " + triple.getMiddle() + " > " + triple.getRight());
        }
        
        writer.close(); this.btnReason.setEnabled(false);
    }//GEN-LAST:event_btnReasonActionPerformed

